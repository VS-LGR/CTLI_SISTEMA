export function normalizeWeightClass(className) {
  if (className == null) return "";
  return String(className).trim().replace(/[–—]/g, "-").toUpperCase();
}

export function toGrams(value, unit = "g") {
  if (!Number.isFinite(value)) return null;
  if (unit === "kg") return value * 1000;
  if (unit === "mg") return value / 1000;
  return value;
}

export function fromGrams(value, unit = "g") {
  if (!Number.isFinite(value)) return null;
  if (unit === "kg") return value / 1000;
  if (unit === "mg") return value * 1000;
  return value;
}

function resolveTableRow(table, nominalInGrams) {
  const nom = Number(nominalInGrams);
  if (!Number.isFinite(nom)) return null;
  if (table[nom]) return table[nom];
  if (table[String(nom)]) return table[String(nom)];
  for (const [k, v] of Object.entries(table)) {
    if (Number(k) === nom) return v;
  }
  return null;
}

function lookupInTable(table, nominalInGrams, className) {
  const cls = normalizeWeightClass(className);
  if (!cls) return null;
  const row = resolveTableRow(table, nominalInGrams);
  if (!row) return null;
  return row[cls] ?? null;
}

/** Incerteza de classe na unidade pedida (g|mg). */
export function lookupClassUncertainty(nominalInGrams, className, unit = "g") {
  const u = String(unit || "g").toLowerCase();
  if (u === "mg") return lookupInTable(UNCERTAINTY_MG, nominalInGrams, className);
  return lookupInTable(UNCERTAINTY_G, nominalInGrams, className);
}

/** Erro máximo admissível δm em mg. */
export function lookupMpeMg(nominalInGrams, className) {
  return lookupInTable(MPE_MG, nominalInGrams, className);
}

export function materialDensityEntry(materialName) {
  if (!materialName) return null;
  const name = String(materialName).trim();
  if (MATERIAL_DENSITY[name]) return MATERIAL_DENSITY[name];
  const lower = name.toLowerCase();
  for (const [k, v] of Object.entries(MATERIAL_DENSITY)) {
    if (k.toLowerCase() === lower) return v;
  }
  return null;
}
